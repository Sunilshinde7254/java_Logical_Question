package Array;

public class Example1_Array 
{public static void main(String[] args) {
	

    int arr[] = {10,20,30,40,50,79};
    
    for(int i =0 ;i<arr.length ;i++)
    {
    	System.out.println(arr[i]);
    }
    
    for(int num:arr)
    {
    	System.out.println(num);
    }
} 
}