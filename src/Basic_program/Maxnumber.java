public class Maxnumber 
{
	public static void main(String[] args) 
	{
        int a = 10, b = 100,c = 80;
        
        if(a > b && a>c)
        {
        	System.out.println(" Max Number: "+a);
        }
        if(b > a && b>c)
        {
        	System.out.println(" Max Number: "+b);
        }
        if(c > a && c>b)
        {
        	System.out.println(" Max Number: "+c);
        }
       
	}
}
